@patch("llm.call_llm")
def test_stage_03_entries_batches_and_resumes(mock_llm, tmp_path):
    # Each call returns 2 entries; target 4 across 1 section
    fake_entries = [
        [{"headword": "a", "pronunciation": "ay", "english_definition": "the letter a"},
         {"headword": "b", "pronunciation": "bee", "english_definition": "the letter b"}],
        [{"headword": "c", "pronunciation": "see", "english_definition": "the letter c"},
         {"headword": "d", "pronunciation": "dee", "english_definition": "the letter d"}],
    ]
    call_count = {"n": 0}

    def side_effect(*args, **kwargs):
        batch = fake_entries[call_count["n"] % len(fake_entries)]
        call_count["n"] += 1
        return fake_llm_result(text=json.dumps(batch))

    mock_llm.side_effect = side_effect

    make_book(tmp_path, status="Structure ready — awaiting review")
    book_dir = tmp_path / "books" / "test-book"
    # Provide a structure doc so parse_sections_from_structure has something
    (book_dir / "02-structure.md").write_text(
        "# Structure\n\n## De Watuh — target: 4\n"
    )

    # Force entry_count_target to 4 so this test finishes quickly
    s = load_state(tmp_path, "test-book")
    s["entry_count_target"] = 4
    save_state(tmp_path, "test-book", s)

    from stage_03_entries import run
    run(tmp_path, "test-book")

    s = load_state(tmp_path, "test-book")
    # After 4 entries collected, status should flip
    assert s["assets_generated"]["entries_count"] >= 4
    assert s["status"] in {"Entries ready — awaiting review", "Entries generating"}


