def fake_llm_result(text: str = "# Fake dossier\n\nGenerated by test.", model: str = "gemini-2.0-pro-exp") -> LLMResult:
    return LLMResult(text=text, tokens_in=100, tokens_out=200, model=model)


def make_book(tmp_path: Path, book_id: str = "test-book", status: str = "Brief received") -> Path:
    """Create a book folder + state.json in a temp books root."""
    s = new_state(
        book_id=book_id,
        working_title="Test Book",
        subtitle="A Test Subtitle",
        prompt_template="vocabulary-reference-v1",
        one_line_brief="A test book brief.",
        entry_count_target=10,  # small for tests
    )
    s["status"] = status
    save_state(tmp_path, book_id, s)
    return tmp_path


@patch("llm.call_llm")
def test_stage_01_dossier(mock_llm, tmp_path):
    mock_llm.return_value = fake_llm_result("# Test dossier\n\nContent here.")
